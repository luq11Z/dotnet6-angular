using System.ComponentModel.DataAnnotations;

namespace SKINET.App.Dtos
{
    public class RegisterDto
    {
        [Required]
        public string DisplayName { get; set; }

        [Required]
        [EmailAddress]
        public string Email { get; set; }

        [Required]
        [RegularExpression("(?=^.{6,15}$)(?=.*\\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[!@#$%^&amp;*()_+}{&quot;:;'?/&gt;.&lt;,])(?!.*\\s).*$", 
            ErrorMessage = "Password must have one Uppercase letter, one Lowercase letter, one non alphanumeric, one number and at least 6 characters")]
        public string Password { get; set; }
    }
}
